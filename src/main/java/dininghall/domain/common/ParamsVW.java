package dininghall.domain.common;

import dininghall.generic.Interface.AColumn;
import dininghall.generic.Interface.ASqlTable;
import dininghall.generic.Interface.ISqlTable;

import java.io.Serializable;

/**
 * @author Tolga
 */
@ASqlTable(TableName = "params_view")
public class ParamsVW implements Serializable, ISqlTable {
    @AColumn(name = "param_id")
    private int paramId;
    @AColumn(name = "param_name")
    private String paramName;
    @AColumn(name = "param_value")
    private String paramValue;
    @AColumn(name = "param_desc")
    private String paramDesc;


    public int getParamId() {
        return paramId;
    }

    public void setParamId(int paramId) {
        this.paramId = paramId;
    }

    public String getParamName() {
        return paramName;
    }

    public void setParamName(String paramName) {
        this.paramName = paramName;
    }

    public String getParamValue() {
        return paramValue;
    }

    public void setParamValue(String paramValue) {
        this.paramValue = paramValue;
    }

    public String getParamDesc() {
        return paramDesc;
    }

    public void setParamDesc(String paramDesc) {
        this.paramDesc = paramDesc;
    }
}
