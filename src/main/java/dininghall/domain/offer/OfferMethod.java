package dininghall.domain.offer;


public class OfferMethod {
    private int offerMethodId;
    private String offerMethodName;

    public int getOfferMethodId() {
        return offerMethodId;
    }

    public void setOfferMethodId(int offerMethodId) {
        this.offerMethodId = offerMethodId;
    }

    public String getOfferMethodName() {
        return offerMethodName;
    }

    public void setOfferMethodName(String offerMethodName) {
        this.offerMethodName = offerMethodName;
    }
}
